    public void carregarTela(caminhoneiroDTO caminhoneiro) {        
        DefaultTableModel dtmMedia = (DefaultTableModel) jTable1.getModel();

        Object [] dados = {caminhoneiro.getCodPessoa(), caminhoneiro.getNome(), caminhoneiro.getIdade(), caminhoneiro.getNumCNH(), caminhoneiro.getTipoCNH(), caminhoneiro.getCodEmpresa()};
        dtmMedia.addRow(dados);
    }
    public caminhoneiroTabelaGUI() {
        initComponents();
        
        this.setLocationRelativeTo(null);
        caminhoneirobo = new caminhoneiroBO();
        listObj = caminhoneirobo.pesquisar();
        for (int x=0; x< listObj.size();x++)
                carregarTela(listObj.get(x));
    }

